package com.company;

public class literals {
    public static void main(String[] args) {
        int age=20; //4 bytes
        byte age2=56; //1 byte
        short age3=57; //2 bytes
        long ageDino=3578254572L; //8 bytes
        char ch='A'; //2 bytes
        float f1=5.6f; //4 bytes
        double d1=67.7; //by default decimal point is double and takes 8 bytes
        boolean a=true;
        String str= "Sanvi";
        System.out.println(age);
        System.out.println(str);
    }
}