package com.company;

public class Operators {
    public static void main(String[] args) {
        int a=9;
        int b=a+3;
        int c=7;
        c+=2;
        System.out.println(b);
        System.out.println(c);
        System.out.println(7>=7);
        System.out.println(6>8 && 5>4); //Logical and
        System.out.println(4<7 || 7>6); //Logical or
        System.out.println(2 & 3); //& is boolean addition

       /* 10
          11
         ----
          10 ie 2     */
    }
}
